function makeWarning() {
	let warningCode =                      '\n' + 
		'<div class="warningBlock">'        + '\n' + 
		'<p class="warningNote">'          + '\n' + 
		'<strong>IMPORTANT NOTE:</strong>' + '\n' + 
		'Because anyone can list their work on Nightlinks, we cannot vet or endorse the character or quality of any of our listings.  You are highly encouraged to do your own research and use your best judgement to find collaborators whose work and values align with your expectations.' + '\n' + 
		'</p> <!-- end warningNote -->'    + '\n' + 
		'</div> <!-- end warningCode -->'  + '\n';
	document.write(warningCode);
}

makeWarning();
